# nginx 
# It is a high performance web server which refers to the physical server and software that is running on machine.
# Server pages, http requests, and responses are processed by the web server.
# It follows event driven architecture.
# It acts as a reverse proxy and a load balancer.
# It can cache static files.
# Centralized logging and monitoring , access control, security.
# Accepts and filters encrypted traffic and rejects unencrypted traffic.
# Reduce the bandwidth usage by compressing files.
# Sends higher size data to the chunks.

# nginx configuration file
# Path of the file - In this file all the configuration for the web server is written.
